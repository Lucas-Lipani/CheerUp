  Future <void> _consultaUsuario2(String nome, String senha) async{
    String usuario_acesso;
    var db = FirebaseFirestore.instance;
    final userRef = db.collection("usuario");
    userRef.where("email_usuario", isEqualTo: nome)
        .where("senha_usuario", isEqualTo: senha).get().then(
          (res) => {
                  for (var doc in res.docs) {
                    usuario_acesso = doc.id,
            },
            if (usuario_acesso != null) {
              Navigator.pushReplacement(
                context,
                MaterialPageRoute(builder: (context) => Inicio(usuario_acesso)),
              )}
            else
              showAlertDialog1(context)
          },
        onError: (e) => showAlertDialog1(context),
    );
  }

